package com.pra1.api.Models;

public class Contenido {
    private int adulto;
    private int parodia;
    private int medico;
    private int violencia;
    private int picante;

    public Contenido() {
    }

    public int getAdulto() {
        return adulto;
    }

    public void setAdulto(int adulto) {
        this.adulto = adulto;
    }

    public int getParodia() {
        return parodia;
    }

    public void setParodia(int parodia) {
        this.parodia = parodia;
    }

    public int getMedico() {
        return medico;
    }

    public void setMedico(int medico) {
        this.medico = medico;
    }

    public int getViolencia() {
        return violencia;
    }

    public void setViolencia(int violencia) {
        this.violencia = violencia;
    }

    public int getPicante() {
        return picante;
    }

    public void setPicante(int picante) {
        this.picante = picante;
    }
}
